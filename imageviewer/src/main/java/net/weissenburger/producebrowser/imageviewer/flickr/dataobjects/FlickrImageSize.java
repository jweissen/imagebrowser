public class FlickrImageSize {
    String label;
    int width;
    int height;
    String source;
    String url;
    String media;

    public String getLabel() {
        return label;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public String getSource() {
        return source;
    }

    public String getUrl() {
        return url;
    }

    public String getMedia() {
        return media;
    }
}
